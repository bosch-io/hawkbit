package org.eclipse.hawkbit.ui.common.data.proxies;

/**
 * Proxy for key-value details.
 */
// TODO: check if we need to implement Serializable and override equals and
// hashcode
public class ProxyKeyValueDetails {

    private final String id;
    private final String key;
    private final String value;

    public ProxyKeyValueDetails(final String id, final String key, final String value) {
        this.id = id;
        this.key = key;
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
